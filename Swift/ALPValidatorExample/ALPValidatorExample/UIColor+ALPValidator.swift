import UIKit

extension UIColor {
    
    class func alpValidatorRed() -> UIColor {
        return UIColor(red: 0.82, green: 0.01, blue: 0.11, alpha: 1)
    }
    
    class func alpValidatorGreen() -> UIColor {
        return UIColor(red: 0.49, green: 0.83, blue: 0.13, alpha: 1)
    }

    class func alpValidatorYellow() -> UIColor {
        return UIColor(red: 0.97, green: 0.91, blue: 0.11, alpha: 1)
    }
    
}